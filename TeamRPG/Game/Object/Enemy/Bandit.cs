        int x = 0;
        int y = 0;
        string gNum = "";
        string name;
        public Bandit(int _x, int _y, string _num) // default는 중앙으로잡고 거기서 위치를잡게끔
        {
            gNum = _num;
            name = "산적" + gNum;
            x = _x;
            y = _y;
        }
        public override void Init()
        {
            nameText = new RawText("산적" + gNum, x, y - 2, HorizontalAlign.Center);
            imageText = new RawText(normalImage, x - 20, y, HorizontalAlign.Center);

            state.name = name;
        }
        public override void SelectEnemy()
        {
            TextIOManager.GetInstance().OutputSmartText("▶", nameText.X - 10, nameText.Y);
        }
        protected override void DrawImage()
        {
            if (isExSkill == false)
            {
                imageText.SetText(normalImage);
            }
            else if (isExSkill == true)
            {
                imageText.SetText(parryImage);
            }
        }

        public override void Release()
        {
            UIManager.GetInstance().RemoveUIElement(nameText);
            UIManager.GetInstance().RemoveUIElement(imageText);
        }
