                state = StatusFactory.GetStatusByEnemy(Core.EnemyManager.eEnemyNum.eLich2);
                isOnce = true;
                isDie = false;
                isParreyFail = false;
                keyPad.Clear();
                isTurn = false;
                isExSkill = false;
                parryStopwatch.Reset();
                parryStopwatch.Stop();
                stopwatch.Start();
                isSkip = true;
                PlayerManager.GetInstance().gameMsg = "리치가 부활하였습니다.";
            }
        }
        protected override void DrawImage()
        {
            if (isExSkill == false && isOnce == false)
            {
                imageText.SetText(normalImage);
            }
            else if (isExSkill == true)
            {
                imageText.SetText(parryImage);
            }
            else
            {
                imageText.SetText(paze2Image);
            }
        }

        public override void Release()
        {
            UIManager.GetInstance().RemoveUIElement(nameText);
            UIManager.GetInstance().RemoveUIElement(imageText);
        }
    }
